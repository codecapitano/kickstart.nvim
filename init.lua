-- Inlay hints: off by default, toggle globally with <leader>th
vim.lsp.inlay_hint.enable(false)
vim.keymap.set('n', '<leader>th', function() vim.lsp.inlay_hint.enable(not vim.lsp.inlay_hint.is_enabled()) end, { desc = 'LSP: [T]oggle Inlay [H]ints' })

-- Diagnostic virtual text: toggle visibility with <leader>td
vim.keymap.set(
  'n',
  '<leader>td',
  function() vim.diagnostic.config { virtual_text = not vim.diagnostic.config().virtual_text } end,
  { desc = '[T]oggle [D]iagnostic virtual text' }
)

-- Exit terminal mode in the builtin terminal with a shortcut that is a bit easier
-- for people to discover. Otherwise, you normally need to press <C-\><C-n>, which
-- is not what someone will guess without a bit more experience.
